  static List<StoryModel> gettarSurahStories = [];

  static List<StoryModel> getStoriesForCategory(String categoryTitle) {
    switch (categoryTitle) {
      case AppConstant.tarbawia:
        return getTarbawiaStories;
      case AppConstant.char:
        return getCharStories;
      case AppConstant.surah:
        return gettarSurahStories;
      default:
        return getTarbawiaStories;
    }
  }

  static int getStoryCount(String id) {
    switch (id) {
      case AppConstant.char:
        return StoryList.getCharStories.length;
      case AppConstant.surah:
        return StoryList.gettarSurahStories.length;
      case AppConstant.tarbawia:
        return StoryList.getTarbawiaStories.length;
      default:
        return 0;
    }
  }
